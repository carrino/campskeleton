    public AutoEnumCombo(Enum num, ValueSet vs) {
        this.vs = vs;

        initComponents();

        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();

        for (Enum e : num.getClass().getEnumConstants())
        {
            dcbm.addElement( e );
        }

        combo.setModel( dcbm );
        combo.setSelectedItem( num );
    }

